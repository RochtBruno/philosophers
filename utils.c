#include "philo.h"

int	ft_atoi(char *str)
{
	int	i;
	int	res;
	int	signal;


	i = 0;
	res = 0;
	signal = 1;

	if (str[i] == '-')
	{
		signal = -1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * signal);
}

int	only_number(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
